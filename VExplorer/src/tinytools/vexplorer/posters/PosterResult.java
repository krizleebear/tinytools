package tinytools.vexplorer.posters;

public class PosterResult
{
	private String posterURL;
	private String nextRessourceURL;

	public String getPosterURL()
	{
		return posterURL;
	}

	public void setPosterURL(String posterURL)
	{
		this.posterURL = posterURL;
	}

	public String getNextRessourceURL()
	{
		return nextRessourceURL;
	}

	public void setNextRessourceURL(String nextRessourceURL)
	{
		this.nextRessourceURL = nextRessourceURL;
	}
}